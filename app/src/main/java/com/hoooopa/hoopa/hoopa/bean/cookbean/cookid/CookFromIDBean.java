public class CookFromIDBean {

    private String code;

    private boolean charge;

    private String msg;

    private FirstLvResultID result;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public boolean isCharge() {
        return charge;
    }

    public void setCharge(boolean charge) {
        this.charge = charge;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public FirstLvResultID getResult() {
        return result;
    }

    public void setResult(FirstLvResultID result) {
        this.result = result;
    }
}
